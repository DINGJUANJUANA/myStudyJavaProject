//key 是string  value是json对象
function setH5Session(key,value){
    sessionStorage.setItem(key, JSON.stringify(value));
}
//返回json对象
function getH5Session(key){
	return JSON.parse(sessionStorage.getItem(key));
}
//字典类型code，字典键值key
function getConfigValByCode2Key(code,key){
    var res ,list = getH5Session('configs');
    for (var i in list){
        if(list[i].code == code && list[i].key == key){
            res = list[i].value;
        }
    }
    return res == null ? "":res;
}

/**
 * var str='这是一个测试的字符串：{0} {1}'.format('Hello','world');
 * var str='这是一个测试的字符串：{str0} {str1}'.format({str0:'Hello',str1:'world'});
 */
String.prototype.format = function(args) {
    var result = this;
    if (arguments.length > 0) {
        if (arguments.length == 1 && typeof (args) == "object") {
            for (var key in args) {
                if(args[key]!=undefined){
                    var reg = new RegExp("({" + key + "})", "g");
                    result = result.replace(reg, args[key]);
                }
            }
        }
        else {
            for (var i = 0; i < arguments.length; i++) {
                if (arguments[i] != undefined) {
                    var reg= new RegExp("({)" + i + "(})", "g");
                    result = result.replace(reg, arguments[i]);
                }
            }
        }
    }
    return result;
}

//获取URL参数
function getUrlParam(name) {
    var reg = new RegExp("(^|&)" + name + "=([^&]*)(&|$)", "i");
    var r = window.location.search.substr(1).match(reg);
    if (r != null) return unescape(r[2]);
    return null;
}